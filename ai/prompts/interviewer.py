def get_random_question() -> tuple[str, str]:
    """Возвращает случайный вопрос и его категорию."""
    category = random.choice(list(QUESTION_CATEGORIES.keys()))
    question = random.choice(QUESTION_CATEGORIES[category])
    return question, category


def get_interview_prompt(category: str = "") -> str:
    """Промпт для генерации нового вопроса AI."""
    cat_hint = f"\nКатегория для следующих вопросов: {category}" if category else ""
    return f"""Ты — AI-интервьюер. Задай Александру (мебельный мастер) вопрос
КАК РЕАЛЬНЫЙ КЛИЕНТ. Вопрос должен быть конкретным и жизненным.{cat_hint}

Формат ответа:
💬 «[вопрос от лица клиента]»

Только один вопрос, без пояснений."""


def get_ai_followup_prompt(
    previous_question: str,
    previous_answer: str,
    all_questions_asked: list[str] | None = None,
) -> str:
    """Промпт для AI: сгенерировать follow-up вопрос на основе предыдущего ответа."""
    asked_text = ""
    if all_questions_asked:
        asked_list = "\n".join(f"- {q}" for q in all_questions_asked[-10:])
        asked_text = f"\n\nУже заданные вопросы (НЕ ПОВТОРЯЙ их):\n{asked_list}"

    return f"""Ты — любопытный клиент, который выбирает мебель на заказ.
Ты только что задал вопрос мастеру и получил ответ.

Твой предыдущий вопрос: «{previous_question}»
Ответ мастера: «{previous_answer[:500]}»
{asked_text}

Задай ОДИН follow-up вопрос, который:
1. ВЫТЕКАЕТ из ответа мастера — копай глубже в тему
2. Конкретный и жизненный, от лица клиента
3. НЕ повторяет уже заданные вопросы
4. Использует «я», «мне», «у меня»

Примеры хороших follow-up:
- Если мастер сказал "МДФ эмаль держится 15 лет" → "А если я случайно поцарапаю — можно локально подкрасить?"
- Если мастер сказал "Кухня 3 м стоит от 150 тыс" → "А если поставить столешницу подешевле — сколько сэкономлю?"

Формат ответа — ТОЛЬКО вопрос, без кавычек и пояснений."""


def get_ai_fresh_question_prompt(
    knowledge_topics: list[str] | None = None,
) -> str:
    """Промпт для AI: сгенерировать совершенно новый вопрос,
    учитывая уже покрытые темы в базе знаний."""
    covered = ""
    if knowledge_topics:
        topics = "\n".join(f"- {t}" for t in knowledge_topics[-20:])
        covered = f"""

Темы, которые УЖЕ покрыты в базе знаний (НЕ спрашивай про них):
{topics}

Спрашивай о чём-то НОВОМ, что ещё не было покрыто."""

    return f"""Ты — потенциальный клиент мебельного мастера Александра (Новосибирск).
Ты ищешь мебель на заказ и хочешь задать вопрос.
{covered}

Придумай ОДИН необычный, но реалистичный вопрос.
Не банальщину типа "сколько стоит", а что-то живое:
- Конкретная жизненная ситуация
- Нестандартный случай
- Вопрос-сравнение
- Вопрос из страха/сомнения
- Вопрос про тренд

Говори от первого лица («я», «мне», «у меня»).
Ответь ТОЛЬКО вопросом, без кавычек и пояснений."""


def pick_question_for_interview(
    current_index: int,
    total: int,
    previous_question: str = "",
    previous_answer: str = "",
    asked_questions: list[str] | None = None,
) -> dict:
    """Выбирает вопрос для интервью: фиксированный или AI-generated.
    
    Стратегия:
    - Первые ~50% вопросов — из фиксированного списка
    - Остальные — AI генерирует follow-up на основе ответов
    
    Returns:
        dict с ключами:
        - "source": "fixed" | "ai_followup" | "ai_fresh"
        - Для fixed: "question", "category"
        - Для ai_*: "prompt" (промпт для AI)
    """
    # Переломный момент: после половины вопросов переходим на AI
    ai_threshold = total // 2  # Для 5 вопросов: 2 фиксированных, 3 AI

    if current_index < ai_threshold:
        # Фиксированный вопрос из базы
        # Исключаем уже заданные
        available = {}
        for cat, questions in QUESTION_CATEGORIES.items():
            remaining = [q for q in questions if q not in (asked_questions or [])]
            if remaining:
                available[cat] = remaining

        if available:
            cat = random.choice(list(available.keys()))
            question = random.choice(available[cat])
            return {
                "source": "fixed",
                "question": question,
                "category": cat,
            }

    # AI follow-up: если есть предыдущий ответ
    if previous_answer and previous_question:
        return {
            "source": "ai_followup",
            "prompt": get_ai_followup_prompt(
                previous_question, previous_answer, asked_questions
            ),
            "category": "ai_followup",
        }

    # AI свежий вопрос: если нет контекста для follow-up
    return {
        "source": "ai_fresh",
        "prompt": get_ai_fresh_question_prompt(),
        "category": "ai_fresh",
    }


