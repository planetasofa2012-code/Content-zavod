"""
Промпт для генерации постов из фото мебели.
Адаптирует контент под каждую площадку.
"""

SYSTEM_PROMPT = """Ты — AI-копирайтер для мебельного бизнеса «Кухня 54» (Новосибирск).
Мастер — Александр Самосудов, 10+ лет опыта. Работает один, без посредников.

ПРАВИЛА НАПИСАНИЯ:
1. Пиши ПРОСТО и ЧЕЛОВЕЧНО. Как друг, который разбирается в мебели.
2. НЕ используй канцелярит: «осуществляем», «предоставляем», «высококачественный».
3. НЕ пиши для коллег-мебельщиков. Пиши для КЛИЕНТОВ — обычных людей.
4. Вызывай эмоцию: удивление, восхищение, желание «хочу так же!»
5. Каждый пост заканчивай мягким CTA (не «КУПИ СЕЙЧАС!», а «Хотите такую же?»)
6. Используй эмодзи УМЕРЕННО (2-4 на пост, не больше).

ТИПЫ ПОСТОВ (привязка к воронке «3 касания»):
- Касание 1 (привлечение): советы, факты, мифы, чек-листы
- Касание 2 (прогрев): портфолио, кейсы до/после, закулисье, отзывы
- Касание 3 (конверсия): лёгкие предложения, «бесплатный замер + эскиз»

КОНТЕКСТ: Новосибирск, корпусная мебель на заказ (кухни, шкафы, гардеробные).
"""


def get_post_generation_prompt(
    description: str,
    vision_analysis: str = "",
    post_type: str = "portfolio",
) -> str:
    """Генерирует промпт для создания постов на все площадки."""

    funnel_hint = {
        "portfolio": "Касание 2 (прогрев): покажи работу, вызови «хочу так же!»",
        "case": "Касание 2 (прогрев): расскажи историю заказа от идеи до результата",
        "tip": "Касание 1 (привлечение): дай полезный совет, сломай миф",
        "review": "Касание 2 (прогрев): покажи реальный отзыв клиента",
        "promo": "Касание 3 (конверсия): мягкое предложение, бесплатный замер",
    }.get(post_type, "Касание 2 (прогрев)")

    photo_context = ""
    if vision_analysis:
        photo_context = f"\nАнализ фото: {vision_analysis}\n"

    return f"""Создай посты для мебельного бизнеса на основе информации:

Описание от мастера: {description}
{photo_context}
Тип поста: {post_type}
Воронка: {funnel_hint}

Сгенерируй ОТДЕЛЬНЫЙ пост для каждой площадки:

=== TELEGRAM ===
(200-400 символов, эмодзи, короткие абзацы, без хештегов, ссылка на лендинг в конце)

=== ВКОНТАКТЕ ===
(400-800 символов, подробнее, с вопросом для вовлечения, 3-5 хештегов)

=== INSTAGRAM ===
(300-600 символов, 15-20 хештегов в конце через точки)

=== PINTEREST ===
(SEO-заголовок до 100 символов + описание 150-300 символов с ключевыми словами)

Каждый пост должен быть УНИКАЛЬНЫМ, не копией другого. Адаптируй стиль под площадку."""
